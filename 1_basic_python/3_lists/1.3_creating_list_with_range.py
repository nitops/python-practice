""" returning list with range function """

list1 = range(3)

""" Just like with zip, the range function returns an object that we can convert into a list """
print(list1)  # output: range(0, 3)
print(type(list1))

""" converting to list"""
print(list(list1))  # output: [0, 1_first_class_fucntions, 2]
